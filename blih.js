count = 0

while (count < 7) {

    if (count.length == 0) {
        console.log("#")
        count += 1
        break

    } if (count.length == 1) {
        console.log("##")
        count += 1
        break

    } if (count.length == 2) {
        console.log("###")
        count += 1
        break

    } if (count.length == 3) {
        console.log("####")
        count += 1
        break

    } if (count.length == 4) {
        console.log("#####")
        count += 1
        break

    } if (count.length == 5) {
        console.log("######")
        count += 1
        break

    } if (count.length == 6) {
        console.log("#######")
        count += 1
        break

    }

}